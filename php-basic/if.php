<?php
// &&（かつ）を記述して、すべての条件が成り立つ場合にのみ処理を行う（変数$numの値が11～29）
if ($num > 10 && $num < 30) {
    echo '変数$numは10より大きく、30より小さいです';
}

// ||（または）を記述して、1つでも条件が成り立てば処理を行う（変数$numの値が10または30）
if ($num === 10 || $num === 30) {
    echo '変数$numは10または30です';
}
?>
